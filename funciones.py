#Función para el área de un triángulo

def triangulo(MEDIDA1, MEDIDA2):
	area = MEDIDA1 * MEDIDA2 / 2
	return area

#Función para el área de un rectángulo

def rectangulo(MEDIDA1, MEDIDA2):
        area = MEDIDA1 * MEDIDA2
        return area

#Función para el área de un círculo

def circulo(MEDIDA1):
	area = 3.1416 * MEDIDA1 ** 2
	return area